" Fold classes and functions in Python files
function! FoldPythonClassesAndFunctions()
    " Enable folding
    setlocal foldmethod=marker
    setlocal foldmarker={,}

    " Find the line number of the test_function() definition
    let lnum = search('^def', 'nw')
    if lnum != 0
        " Move to the line before the function definition
        execute 'normal! ' . (lnum - 1) . 'G'

        " Create the fold marker
        normal! zf%
    endif
endfunction

